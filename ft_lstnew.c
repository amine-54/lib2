#include <stdlib.h>
#include <stdio.h>
#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = (t_list *)malloc(sizeof(t_list));
	if (node == NULL)
		return (NULL);
	node->content = content;
	node->next = NULL;
	return (node);
}

int main()
{
	t_list *tst;
	int	i;

	i = 15;
	tst = ft_lstnew(&i);
	printf("%d", *(int *)(tst->content));
}
